#include "libft.h"

char	*ft_strndup(const char *s, size_t n)
{
	char	*new_string;
	size_t	i;

	if (!(new_string = (char*)malloc(sizeof(char) * n + 1)))
		return (NULL);
	i = 0;
	while (i < n && s[i])
	{
		new_string[i] = s[i];
		i++;
	}
	new_string[i] = '\0';
	return (new_string);
}
